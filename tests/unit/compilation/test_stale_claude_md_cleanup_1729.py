"""Regression tests for issue #1729 -- stale CLAUDE.md never removed after
instructions move to .claude/rules/.

Acceptance criteria:
- Existing APM-generated CLAUDE.md + populated .claude/rules/ + apm compile --clean
  -> CLAUDE.md removed.
- Hand-authored CLAUDE.md (no marker) is NEVER removed; warning emitted instead.
- Plain apm compile (no --clean) still deletes nothing (non-destructive).
- Clean-checkout behavior from #1138 unchanged (no CLAUDE.md ever created when
  .claude/rules/ already populated).
- --no-dedup / --force-instructions still regenerate a full CLAUDE.md and never
  trigger removal.
"""

from __future__ import annotations

import pytest

from apm_cli.compilation.agents_compiler import AgentsCompiler, CompilationConfig
from apm_cli.compilation.claude_formatter import CLAUDE_HEADER
from apm_cli.compilation.constitution import clear_constitution_cache
from apm_cli.primitives.models import Instruction, PrimitiveCollection

# ---------------------------------------------------------------------------
# Module-level cache isolation
# ---------------------------------------------------------------------------


@pytest.fixture(autouse=True)
def _reset_constitution_cache():
    """Clear the module-level constitution cache before and after every test.

    The cache is global; without this fixture a test that places a constitution
    file on disk can leak state into later tests and cause unexpected
    CLAUDE.md emission or suppressed removal previews.
    """
    clear_constitution_cache()
    yield
    clear_constitution_cache()


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------


def _make_project(tmp_path, *, populate_rules: bool = True):
    """Create a minimal project with an instruction primitive.

    If populate_rules=True a .claude/rules/*.md file is created so that
    _detect_deployed_instructions returns True (skip_instructions fires).
    """
    (tmp_path / "apm.yml").write_text(
        "name: test-stale-claude\nversion: 1.0.0\ntargets:\n  - claude\n",
        encoding="utf-8",
    )
    instr_dir = tmp_path / ".apm" / "instructions"
    instr_dir.mkdir(parents=True)
    (instr_dir / "global.instructions.md").write_text(
        "---\ndescription: A global instruction\n---\nThis is global guidance.\n",
        encoding="utf-8",
    )
    if populate_rules:
        rules_dir = tmp_path / ".claude" / "rules"
        rules_dir.mkdir(parents=True)
        (rules_dir / "global.md").write_text("This is global guidance.\n", encoding="utf-8")


def _apm_generated_claude_md(tmp_path) -> None:
    """Write a CLAUDE.md that looks like one generated by APM (has CLAUDE_HEADER)."""
    (tmp_path / "CLAUDE.md").write_text(
        f"{CLAUDE_HEADER}\n\n# Project Standards\n\nThis is global guidance.\n",
        encoding="utf-8",
    )


def _hand_authored_claude_md(tmp_path) -> None:
    """Write a CLAUDE.md without the APM marker (hand-authored)."""
    (tmp_path / "CLAUDE.md").write_text(
        "# My Custom Instructions\n\nHand-written content.\n",
        encoding="utf-8",
    )


def _make_primitives(tmp_path) -> PrimitiveCollection:
    primitives = PrimitiveCollection()
    primitives.add_primitive(
        Instruction(
            name="global",
            file_path=tmp_path / ".apm/instructions/global.instructions.md",
            description="A global instruction",
            apply_to="",
            content="This is global guidance.",
            author="test",
            source="local",
        )
    )
    return primitives


# ---------------------------------------------------------------------------
# Main acceptance criterion: --clean removes APM-generated stale CLAUDE.md
# ---------------------------------------------------------------------------


class TestCleanRemovesApmGeneratedStaleCLAUDEMD:
    """apm compile --clean must remove a stale APM-generated CLAUDE.md when
    instructions are already in .claude/rules/ (issue #1729 primary fix)."""

    def test_clean_removes_apm_generated_claude_md(self, tmp_path):
        """--clean + .claude/rules/ populated + APM-generated CLAUDE.md -> file removed."""
        _make_project(tmp_path, populate_rules=True)
        _apm_generated_claude_md(tmp_path)

        compiler = AgentsCompiler(str(tmp_path))
        config = CompilationConfig(
            target="claude",
            clean_orphaned=True,
            dry_run=False,
        )
        primitives = _make_primitives(tmp_path)
        result = compiler._compile_claude_md(config, primitives)

        assert result.success, f"Expected success, got errors: {result.errors}"
        assert not (tmp_path / "CLAUDE.md").exists(), (
            "APM-generated CLAUDE.md must be removed by --clean when .claude/rules/ is populated"
        )

    def test_clean_log_mentions_removal(self, tmp_path):
        """A success-level log about the removal must be emitted (green/bold)."""
        _make_project(tmp_path, populate_rules=True)
        _apm_generated_claude_md(tmp_path)

        logged: list[tuple[str, str]] = []

        class _CapturingLogger:
            def progress(self, msg, **kw):
                logged.append(("progress", msg))

            def success(self, msg, **kw):
                logged.append(("success", msg))

            def warning(self, msg, **kw):
                logged.append(("warning", msg))

            def verbose_detail(self, msg, **kw):
                pass

        compiler = AgentsCompiler(str(tmp_path))
        compiler._logger = _CapturingLogger()
        config = CompilationConfig(
            target="claude",
            clean_orphaned=True,
            dry_run=False,
        )
        primitives = _make_primitives(tmp_path)
        compiler._compile_claude_md(config, primitives)

        # Must be logged at "success" level (green) -- a destructive file deletion
        # must render visibly distinct from an info/progress no-op (issue #1729 fix).
        success_messages = [m for level, m in logged if level == "success"]
        assert any("Removed stale" in m and "CLAUDE.md" in m for m in success_messages), (
            f"Expected a 'Removed stale CLAUDE.md' log line at 'success' level, got: {logged}"
        )


