class TestNoDedupPreventsRemoval:
    """When --no-dedup is set, instructions are always written into CLAUDE.md
    and the stale-file-removal path must not fire."""

    def test_no_dedup_writes_claude_md_despite_rules_dir(self, tmp_path):
        """--no-dedup regenerates CLAUDE.md even when .claude/rules/ is populated;
        no removal must happen."""
        _make_project(tmp_path, populate_rules=True)
        # Start with an APM-generated file that could mistakenly be deleted
        _apm_generated_claude_md(tmp_path)

        compiler = AgentsCompiler(str(tmp_path))
        config = CompilationConfig(
            target="claude",
            clean_orphaned=True,
            no_dedup=True,
            dry_run=False,
        )
        primitives = _make_primitives(tmp_path)
        compiler._compile_claude_md(config, primitives)

        # File must still exist (now regenerated with full instructions)
        assert (tmp_path / "CLAUDE.md").exists(), (
            "--no-dedup must produce a CLAUDE.md; stale-removal must not fire"
        )
        body = (tmp_path / "CLAUDE.md").read_text(encoding="utf-8")
        assert "# Project Standards" in body or "global guidance" in body.lower(), (
            "--no-dedup CLAUDE.md must contain instruction content"
        )

    def test_no_dedup_does_not_remove_without_rules(self, tmp_path):
        """--no-dedup on a project with no .claude/rules/ writes CLAUDE.md normally."""
        _make_project(tmp_path, populate_rules=False)

        compiler = AgentsCompiler(str(tmp_path))
        config = CompilationConfig(
            target="claude",
            no_dedup=True,
            dry_run=False,
        )
        primitives = _make_primitives(tmp_path)
        result = compiler._compile_claude_md(config, primitives)

        assert result.success
        assert (tmp_path / "CLAUDE.md").exists()


